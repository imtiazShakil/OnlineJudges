ll rec(int set1,int indx)
{
    if(indx==n+1)
    {
        if(sum-set1==set1)
        {
            return 1;
        }
        else return 0;
    }

    ll &ret=dp[set1][indx];
    if(ret!=-1) return ret;
    ret=0;

    ret=rec(set1+indx,indx+1);
    ret+=rec(set1,indx+1);

    return ret;
}

